        public string getFirstName(char gender = ' ')
        {
            var rand = new Random();

            switch (gender)
            {
                case 'm': case 'M':
                    return MFirstNames[rand.Next(MFirstNames.Length)];
                case 'f': case 'F':
                    return FFirstNames[rand.Next(FFirstNames.Length)];
                default:
                    if (rand.Next(1) == 1)
                        return MFirstNames[rand.Next(MFirstNames.Length)];
                    else return FFirstNames[rand.Next(FFirstNames.Length)];
            }
        }

